class TransformerBlock(nn.Module):
    def __init__(self, dim, num_heads, ffn_expansion_factor, bias, LayerNorm_type):
        super(TransformerBlock, self).__init__()

        self.norm1 = LayerNorm(dim, LayerNorm_type)
        self.attn = Attention(dim, num_heads, bias)
        self.norm2 = LayerNorm(dim, LayerNorm_type)
        self.ffn = FeedForward(dim, ffn_expansion_factor, bias)

    def forward(self, x):
        x = x + self.attn(self.norm1(x))
        x = x + self.ffn(self.norm2(x))

        return x


class Restormer(nn.Module):
    def __init__(self,
                 dim=64,
                 num_blocks=4,
                 heads=8,
                 ffn_expansion_factor=2,
                 bias=False,
                 LayerNorm_type='WithBias',
                 ):
        super(Restormer, self).__init__()

        self.restormer = nn.Sequential(*[TransformerBlock(dim=dim, num_heads=heads, ffn_expansion_factor=ffn_expansion_factor,
                                                          bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks)])

    def forward(self, in_features):
        in_features = self.restormer(in_features)

        return in_features


# 基于 Swin Transformer 的全局特征提取模块
class Transformer_Based_Model(nn.Module):
    def __init__(self, in_channels):
        super(Transformer_Based_Model, self).__init__()
        self.restormer = Restormer(dim=in_channels, num_blocks=2, heads=8, ffn_expansion_factor=2, bias=False, LayerNorm_type='WithBias')

    def forward(self, x):
        x = self.restormer(x) + x
        return x


